package inheritSample;

public class Car {

	Car() {
		System.out.println("車です。");

	}

	public void drive() {
		System.out.println("車が走ります。");
	}

	public void stop() {
		System.out.println("車が止まります。");
	}

	public void turnLeft() {
		System.out.println("車が左に曲がります。");
	}

	public void turnRight() {
		System.out.println("車が右に曲がります。");
	}

}
